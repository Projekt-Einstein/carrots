public class Zadanie2 {

    public static void main(String[] args) {
        /////////
        // for //
        ////////
        for (int i = 0; i < 100; i++) {
            System.out.println(i);
        }

        int i = 0;

        ///////////
        // while //
        ///////////
        while (i < 100) {
            System.out.println(i);
            i++;
        }

        i = 0;

        //////////////
        // do-while //
        //////////////
        do {
            System.out.println(i);
            i++;

        } while (i < 100);
    }

}
